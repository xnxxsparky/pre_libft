#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	if (lst != NULL && f != NULL)
	{
		while (lst->content)
		{
			(*f)(lst);
			lst = lst->next;
		}
	}
}
